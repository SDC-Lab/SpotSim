package org.cloudbus.spotsim.random;

public class MixtureModel {

    private int k;

    private double[] p;

    private double[] mu;

    private double[] sigma;

	public MixtureModel(int k, double[] p, double[] mu, double[] sigma) {
		super();
		this.k = k;
		this.p = p;
		this.mu = mu;
		this.sigma = sigma;
	}

	public int getK() {
		return k;
	}

	public void setK(int k) {
		this.k = k;
	}

	public double[] getP() {
		return p;
	}

	public void setP(double[] p) {
		this.p = p;
	}

	public double[] getMu() {
		return mu;
	}

	public void setMu(double[] mu) {
		this.mu = mu;
	}

	public double[] getSigma() {
		return sigma;
	}

	public void setSigma(double[] sigma) {
		this.sigma = sigma;
	}
    
    
}